// Helper function to link lesson plans to periods for integration testing
export function getMockPeriodsWithLessonLinks(): ClassPeriod[] {
  const periods = initializeMockClassPeriods(mockTeachers);
  const lessonPlans = initializeMockLessonPlans(mockTeachers);

  // Create a map of lessons by class, subject, and time for easy matching
  const lessonMap = new Map<string, LessonPlan[]>();
  lessonPlans.forEach((lesson) => {
    const key = `${lesson.classId}-${lesson.subject}-${lesson.startTime}-${lesson.endTime}`;
    if (!lessonMap.has(key)) {
      lessonMap.set(key, []);
    }
    lessonMap.get(key)!.push(lesson);
  });

  // Link lessons to periods based on matching criteria
  return periods.map((period) => {
    const key = `${period.classId}-${period.subject}-${period.startTime}-${period.endTime}`;
    const matchingLessons = lessonMap.get(key) || [];

    // For integration: link some periods to lessons (about 60-70% linked)
    if (matchingLessons.length > 0 && Math.random() > 0.35) {
      // Pick a random matching lesson
      const randomLesson = matchingLessons[Math.floor(Math.random() * matchingLessons.length)];
      return {
        ...period,
        lessonPlanId: randomLesson.id,
      };
    }

    return period;
  });
}

